# kali ini akan belajar operasi logika

# operasi logika menggunkan simbol and, or, not

# operasi (and) hasilnya akan True jika kedua statement True
print("Operasi logika AND")
a = True
b = True
print("niali a adalah: ", a)
print("niali b adalah: ", b)
c = a and b
print(" apakah a and b : ", c)
print("=" * 10)

# operasi (or) hasilny akan True jika ada statement True
print("Operasi logika OR")
a = False
b = True
print("niali a adalah: ", a)
print("niali b adalah: ", b)
c = a or b
print("apakah a or b : ", c)
print("=" * 10)

# operasi (not) hasilnya kebalikan jika hasilnya True
print("Operasi logika NOT")
a = False
print("niali a adalah: ", a)
c = not b
print("apakah a not b : ", c)
