# Lanjutan dari if else mendalam

nilai = int(input("Tolong beri bintang pelayan kami:[1,2,3,4,5]"))

if nilai == 1:
    print("Terima kasih atas penilaiannya, kirim masukan dan saran karna anda memberi bintang 1")

elif nilai == 2:
    print("Terima kasih atas penilaiannya, jika berkenan tolong berikan saran agar pelayan kami mendapat bintang lebih")

elif nilai == 3:
    print("Terima kasih atas penilaiannya, kami akan berusaha agar memenuhi kepuasan anda")

elif nilai == 4:
    print("terima kasih atas penilaiannya, kami akan memberikan anda diskon atas penilaian tersebut")

elif nilai == 5:
    print("Terima kasih atas penilaiannya, anda telah mendapatkan VIP service")

else:
    print("Tolong pilih antara 1, 2, 3, 4, 5 terima kasih")
